#!/usr/bin/env python3
"""Automatic Dataset Collection for YOLO Training.

Collects synchronized camera images and ground truth annotations
from Isaac Sim for YOLO model training.

Classes (species only, NOT alive/dead):
- 0: sturgeon
- 1: salmon  
- 2: debris

Usage:
    python fish_collect_dataset.py --duration 300 --output_dir dataset/
    
Or with ROS2:
    ros2 run aqua_detection fish_collect_dataset --ros-args -p duration:=300
"""

import argparse
import json
import os
import time
from datetime import datetime
from pathlib import Path
from typing import Dict, List, Optional

import cv2
import numpy as np

try:
    import rclpy
    from rclpy.node import Node
    from rclpy.qos import qos_profile_sensor_data
    from sensor_msgs.msg import Image
    from std_msgs.msg import String
    from cv_bridge import CvBridge
    ROS_AVAILABLE = True
except ImportError:
    ROS_AVAILABLE = False
    print("ROS2 not available. Running in standalone mode.")


# YOLO class mapping (species only, NOT alive/dead)
CLASS_MAP = {
    "sturgeon": 0,
    "salmon": 1,
    "debris": 2,
}


class DatasetCollector:
    """Collects images and annotations for YOLO training."""
    
    def __init__(
        self,
        output_dir: str = "dataset",
        image_format: str = "jpg",
        split_ratio: float = 0.8,
    ):
        self.output_dir = Path(output_dir)
        self.image_format = image_format
        self.split_ratio = split_ratio
        
        # Create directory structure
        self.images_train = self.output_dir / "images" / "train"
        self.images_val = self.output_dir / "images" / "val"
        self.labels_train = self.output_dir / "labels" / "train"
        self.labels_val = self.output_dir / "labels" / "val"
        
        for d in [self.images_train, self.images_val, self.labels_train, self.labels_val]:
            d.mkdir(parents=True, exist_ok=True)
        
        self.sample_count = 0
        self.train_count = 0
        self.val_count = 0
    
    def save_sample(
        self,
        image: np.ndarray,
        annotations: List[dict],
        pool_id: int,
        frame_id: int,
    ) -> bool:
        """Save image and YOLO annotations.
        
        Args:
            image: BGR image
            annotations: List of fish/debris annotations with bbox_2d
            pool_id: Pool identifier
            frame_id: Frame number
            
        Returns:
            True if saved successfully
        """
        if len(annotations) == 0:
            return False
        
        # Determine train/val split
        is_train = (self.sample_count % 10) < (self.split_ratio * 10)
        
        # File paths
        filename = f"pool{pool_id}_frame{frame_id:06d}"
        if is_train:
            img_path = self.images_train / f"{filename}.{self.image_format}"
            lbl_path = self.labels_train / f"{filename}.txt"
            self.train_count += 1
        else:
            img_path = self.images_val / f"{filename}.{self.image_format}"
            lbl_path = self.labels_val / f"{filename}.txt"
            self.val_count += 1
        
        # Save image
        h, w = image.shape[:2]
        cv2.imwrite(str(img_path), image)
        
        # Convert annotations to YOLO format
        yolo_lines = []
        for ann in annotations:
            bbox = ann.get("bbox_2d")
            if bbox is None:
                continue
            
            species = ann.get("species", "sturgeon")
            class_id = CLASS_MAP.get(species, 0)
            
            # YOLO format: class x_center y_center width height (normalized)
            x, y, bw, bh = bbox
            x_center = (x + bw / 2) / w
            y_center = (y + bh / 2) / h
            width = bw / w
            height = bh / h
            
            # Clamp to [0, 1]
            x_center = max(0.0, min(1.0, x_center))
            y_center = max(0.0, min(1.0, y_center))
            width = max(0.0, min(1.0, width))
            height = max(0.0, min(1.0, height))
            
            yolo_lines.append(f"{class_id} {x_center:.6f} {y_center:.6f} {width:.6f} {height:.6f}")
        
        # Save labels
        with open(lbl_path, "w") as f:
            f.write("\n".join(yolo_lines))
        
        self.sample_count += 1
        return True
    
    def create_yaml_config(self) -> str:
        """Create YOLO dataset configuration file."""
        yaml_content = f"""# AquaSweep Fish Detection Dataset
# Auto-generated by fish_collect_dataset.py
# Date: {datetime.now().isoformat()}

path: {self.output_dir.absolute()}
train: images/train
val: images/val

# Classes (species only, NOT alive/dead - DINOv2 handles status)
names:
  0: sturgeon
  1: salmon
  2: debris

# Dataset statistics
# Total samples: {self.sample_count}
# Train: {self.train_count}
# Val: {self.val_count}
"""
        
        yaml_path = self.output_dir / "fish_species.yaml"
        with open(yaml_path, "w") as f:
            f.write(yaml_content)
        
        return str(yaml_path)
    
    def get_stats(self) -> dict:
        return {
            "total_samples": self.sample_count,
            "train_samples": self.train_count,
            "val_samples": self.val_count,
            "output_dir": str(self.output_dir),
        }


class DatasetCollectorNode(Node):
    """ROS2 node for collecting dataset from Isaac Sim."""
    
    def __init__(self, output_dir: str, duration: float, num_pools: int = 7):
        super().__init__('fish_dataset_collector')
        
        self.collector = DatasetCollector(output_dir=output_dir)
        self.bridge = CvBridge()
        self.duration = duration
        self.num_pools = num_pools
        self.start_time = None
        
        # State per pool
        self.latest_images: Dict[int, np.ndarray] = {}
        self.latest_gt: Dict[int, List[dict]] = {}
        self.frame_counts: Dict[int, int] = {i: 0 for i in range(1, num_pools + 1)}
        
        # Subscribers
        self.img_subs = {}
        self.gt_subs = {}
        
        for pool_id in range(1, num_pools + 1):
            # Image subscriber
            self.img_subs[pool_id] = self.create_subscription(
                Image,
                f'/pool_{pool_id}/top_img_raw',
                lambda msg, pid=pool_id: self._image_callback(msg, pid),
                qos_profile_sensor_data,
            )
            
            # GT subscriber (from fish_gt_publisher in top_cam_ext)
            self.gt_subs[pool_id] = self.create_subscription(
                String,
                f'/pool_{pool_id}/fish_gt',
                lambda msg, pid=pool_id: self._gt_callback(msg, pid),
                10
            )
        
        # Timer for periodic saving
        self.timer = self.create_timer(0.5, self._save_samples)
        
        self.get_logger().info(
            f"Dataset Collector started. "
            f"Duration: {duration}s, Output: {output_dir}"
        )
    
    def _image_callback(self, msg: Image, pool_id: int):
        """Handle incoming camera image."""
        if self.start_time is None:
            self.start_time = time.time()
        
        try:
            cv_image = self.bridge.imgmsg_to_cv2(msg, desired_encoding="bgr8")
            self.latest_images[pool_id] = cv_image
            self.frame_counts[pool_id] += 1
        except Exception as e:
            self.get_logger().error(f"Image conversion failed: {e}")
    
    def _gt_callback(self, msg: String, pool_id: int):
        """Handle incoming ground truth data."""
        try:
            gt_data = json.loads(msg.data)
            fish_list = gt_data.get(f"pool_{pool_id}", [])
            self.latest_gt[pool_id] = fish_list
        except Exception as e:
            self.get_logger().error(f"GT parsing failed: {e}")
    
    def _save_samples(self):
        """Periodically save synchronized image-GT pairs."""
        # Check duration
        if self.start_time and (time.time() - self.start_time) > self.duration:
            self._finish()
            return
        
        # Save samples for each pool
        for pool_id in range(1, self.num_pools + 1):
            if pool_id in self.latest_images and pool_id in self.latest_gt:
                image = self.latest_images[pool_id]
                annotations = self.latest_gt[pool_id]
                frame_id = self.frame_counts[pool_id]
                
                if self.collector.save_sample(image, annotations, pool_id, frame_id):
                    self.get_logger().info(
                        f"Saved sample: pool_{pool_id}, frame_{frame_id}, "
                        f"annotations={len(annotations)}"
                    )
                
                # Clear to avoid duplicates
                del self.latest_images[pool_id]
                del self.latest_gt[pool_id]
    
    def _finish(self):
        """Finish collection and create config."""
        yaml_path = self.collector.create_yaml_config()
        stats = self.collector.get_stats()
        
        self.get_logger().info(
            f"Dataset collection complete!\n"
            f"  Total samples: {stats['total_samples']}\n"
            f"  Train: {stats['train_samples']}\n"
            f"  Val: {stats['val_samples']}\n"
            f"  Config: {yaml_path}"
        )
        
        # Shutdown
        rclpy.shutdown()


def main_ros2():
    """Run with ROS2."""
    rclpy.init()
    
    # Parse arguments
    import sys
    output_dir = "dataset"
    duration = 300.0
    
    for i, arg in enumerate(sys.argv):
        if arg == "--output_dir" and i + 1 < len(sys.argv):
            output_dir = sys.argv[i + 1]
        elif arg == "--duration" and i + 1 < len(sys.argv):
            duration = float(sys.argv[i + 1])
    
    node = DatasetCollectorNode(output_dir=output_dir, duration=duration)
    
    try:
        rclpy.spin(node)
    except (KeyboardInterrupt, SystemExit):
        pass
    finally:
        node.destroy_node()


def main_standalone():
    """Run in standalone mode (for testing without ROS2)."""
    parser = argparse.ArgumentParser(description="Collect dataset for YOLO training")
    parser.add_argument("--output_dir", default="dataset", help="Output directory")
    parser.add_argument("--duration", type=float, default=300, help="Collection duration (seconds)")
    args = parser.parse_args()
    
    print(f"Standalone mode - creating empty dataset structure at {args.output_dir}")
    
    collector = DatasetCollector(output_dir=args.output_dir)
    
    # Create sample annotation for testing
    test_image = np.zeros((720, 1280, 3), dtype=np.uint8)
    test_annotations = [
        {"species": "sturgeon", "bbox_2d": [100, 100, 200, 150]},
        {"species": "debris", "bbox_2d": [500, 300, 50, 50]},
    ]
    
    collector.save_sample(test_image, test_annotations, pool_id=1, frame_id=0)
    yaml_path = collector.create_yaml_config()
    
    print(f"Created test dataset at {args.output_dir}")
    print(f"YOLO config: {yaml_path}")


def main():
    if ROS_AVAILABLE:
        main_ros2()
    else:
        main_standalone()


if __name__ == "__main__":
    main()
